import math

print(round(2.5))  # rounds to the nearest even number
print(abs(-2.5))  # absolute value

# math module -> it is a module that contains mathematical functions
print(math.ceil(2.5))  # rounds up to the nearest integer
print(math.floor(2.5))  # rounds down to the nearest integer
print(math.sqrt(2.5))  # square root
print(math.pow(2.5, 2))  # power
print(math.pi)  # pi
print(math.e)  # e
print(math.isnan(2.5))  # returns True if the value is not a number
print(math.isinf(2.5))  # returns True if the value is infinity
